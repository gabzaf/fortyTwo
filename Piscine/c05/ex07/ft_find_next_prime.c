#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (i <= nb / i)
	{
		if (nb % i == 0)
			return (0);
		else
			i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	while (nb < 2147483647 && !ft_is_prime(nb))
		nb++;
	return (nb);
}
int	main(void)
{
	printf("%d", ft_find_next_prime(35584000));
	return (0);
}
